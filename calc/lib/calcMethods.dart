class CalcMethods {
  int a = 0;
  int b = 0;

  static int summation(int a, int b) {
    return a + b;
  }

  static int subtraction(int a, int b) {
    return a - b;
  }

  static int multiplication(int a, int b) {
    return a * b;
  }

  static double division(int a, int b) {
    return a / b;
  }
}
